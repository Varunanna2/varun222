class switchcase
    {
     public static void main(String args[])
      {
        switch('d')
		{
                case 'a':System.out.println("case1");  
                 break;
                 case 'b':System.out.println("case2");
                break;
                case 'c':System.out.println("case3");
                 break;
                default:System.out.println("false");
         	}
      }
}