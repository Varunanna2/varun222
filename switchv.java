class switchv
    {
     public static void main(String args[])
      {
         char temp='s';
        switch(temp)
               {
                   case 'A':
                    case 'E':
                      case 'I':
                       case 'O':
                        case 'U':
                System.out.println("VOWELS");
                 break;
                    case 'a':
                    case 'e':
                      case 'i':
                       case 'o':
                        case 'u':
                System.out.println("vowels");
                 break;
                default:System.out.println("consonants");
         	}
		
      }
}