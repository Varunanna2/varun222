public class circle
{
    public static void main(String[]args)
    {
        double radius=7;
        double pie=3.14;
        double areaofcircle=pie*radius*radius;
        double areaofcircumference=2*pie*radius;
        System.out.println("area of circle="+areaofcircle);
        System.out.println("area of circumference="+areaofcircumference);
        
        
    }
}